#[cfg(test)]
mod tests {
    use super::{PI, SQRT_2, SQRT_PI};
    use russell_chk::assert_approx_eq;

    #[test]
    fn constants_are_correct() {
        assert_eq!(PI, std::f64::consts::PI);
        assert_approx_eq!(SQRT_PI, f64::sqrt(PI), 1e-15);
        assert_eq!(SQRT_2, 2_f64.sqrt());
    }
}
